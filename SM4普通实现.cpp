uint32_t Sbranch(uint32_t X, int n)
{
    uint32_t data = 0;
    uint32_t temp = 0;
    uint8_t high = 0;
    uint8_t low = 0;
    temp = (X & (0xFF << n)) >> n;
    high = (temp & 0xF0) >> 4;
    low = temp & 0xF;
    data ^= getStrans(high, low) << n;
    return data;
}
